package Week4.Tues;

public class ComparisonOperator {
    public static void main(String[] args) {

        //example of comparisonoperator
        int a = 10;
        int b = 20;

        System.out.println("a > b: " + (a > b)); // false
        System.out.println("a < b: " + (a < b)); // true
        System.out.println("a == b: " + (a == b)); // false
        System.out.println("a != b: " + (a != b)); // true
        System.out.println("a >= b: " + (a >= b)); // false
        System.out.println("a <= b: " + (a <= b)); // true


    }
}
