package Week4.Tues;

public class IncrementDecrement {
    public static void main(String[] args) {

        int x = 10;
        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);

        int y = 20;
        int z = y++;

        System.out.println("y: " + y);
        System.out.println("z: " + z);

    }
}
